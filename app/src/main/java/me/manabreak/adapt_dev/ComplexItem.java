package me.manabreak.adapt_dev;

public class ComplexItem {
    private String first;
    private String second;

    public ComplexItem(String first, String second) {
        this.first = first;
        this.second = second;
    }

    public String getFirst() {
        return first;
    }

    public String getSecond() {
        return second;
    }
}
